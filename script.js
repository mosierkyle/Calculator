'use strict'

function addition(num1, num2) {
    return (num1) + (num2);
  };
  
function subtraction(num1, num2) {
    return (num1) - (num2);
  };
  
function multiplication(num1, num2) {
    return (num1) * (num2);
  };
  
function division(num1, num2) {
    return (num1) / (num2);
  }

function sign(num) {
    return num * (-1);
}

function percent(num) {
    return num / 100
}